#include "libft.h"

char	*ft_strdup(const char *s1)
{
	int		size;
	int		i;
	char	*s2;

	size = ft_strlen(s1) + 1;
	i = 0;
	s2 = (char *)malloc(size * sizeof(char));
	if (!s2)
		return (NULL);
	while (s1[i])
	{
		s2[i] = s1[i];
		i++;
	}
	s2[i] = '\0';
	return (s2);
}
